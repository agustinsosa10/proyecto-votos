    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        String contra = String.valueOf(txtContrasenia.getPassword());
        String contra1 = String.valueOf(txtContrasenia2.getPassword());
        String usuario = txtUsuario.getText();
        try{
            ResultSet rs;
            PreparedStatement ps;
            
            String consulta = "select * from login where usuario = (?) ";
            
            ps = Conexion.estableceConexion().prepareStatement(consulta);
            ps.setString(1,usuario);
            rs = ps.executeQuery();
            
            if(rs.next()){ 
                String user = rs.getNString("usuario");
                if(user.equals(usuario)) {
                    JOptionPane.showMessageDialog(null,"El usuario ya se encuentra registrado");
                    registrarIncidencia("ERROR","EL USUARIO YA SE ENCUENTRA REGISTRADO");
                    limpiarTxt(txtUsuario,txtContrasenia,txtContrasenia2);
                        
                    }                 
            }else{
                if(contra.equals(contra1)){    
                    cargarDatos(usuario,contra);
                    JOptionPane.showMessageDialog(null, "Usuario cargado con exito!");
                    this.dispose();
                    Menu menu = new Menu();
                    menu.setVisible(true);
                    menu.setLocationRelativeTo(null);
                }else{
                    txtContrasenia.setText("");
                    txtContrasenia2.setText("");
                    JOptionPane.showMessageDialog(null, "Debe ser la misma contrasenia");
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"ERROR "+e.toString());
        }
    }//GEN-LAST:event_btnCargarActionPerformed

