    private void cargarDatos (String usuario,String contra) {
        try {           
            String consulta = "INSERT INTO login (usuario, Contrasenia) VALUES (?, ?)";
            PreparedStatement ps = Conexion.estableceConexion().prepareStatement(consulta);
            ps.setString(1, usuario);
            ps.setString(2, contra);
            int rowsInserted = ps.executeUpdate();
            registrarIncidencia("INFO","SE REGISTRO UN NUEVO USUARIO");
        } catch(SQLException e){
            registrarIncidencia("ERROR","NO SE PUDO REGISTRAR EL NUEVO USUARIO");
            JOptionPane.showMessageDialog(null, "Error"+e.toString());
        }
    }
    
    private void limpiarTxt(JTextField usuario, JPasswordField contrasenia, JPasswordField contrasenia1){
        usuario.setText("");
        contrasenia.setText("");
        contrasenia1.setText("");
    }
